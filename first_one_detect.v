// 首个1检测模块 (First One Detector)
// 
// 这是 BitColor 论文的核心组件！
// 功能：在32位输入中找到最低位的1的位置
// 
// 论文观察1：通过位运算在单周期内确定可用颜色
// - 邻居颜色通过 OR 操作收集
